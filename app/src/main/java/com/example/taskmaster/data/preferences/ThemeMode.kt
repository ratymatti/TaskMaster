package com.example.taskmaster.data.preferences

/**
 * Represents the available theme modes for the application.
 */
enum class ThemeMode {
    /**
     * Always use light theme regardless of system settings
     */
    LIGHT,

    /**
     * Always use dark theme regardless of system settings
     */
    DARK,

    /**
     * Follow the system theme setting (default)
     */
    SYSTEM
}

